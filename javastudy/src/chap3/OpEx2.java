package chap3;

public class OpEx2 {

	public static void main(String[] args) {
		// 부호 연산자
		// + -> 현재 부호 값 유지해
		// - -> 부호 바꿔
		int x = -10;
		System.out.println("+x=" + +x);
		System.out.println("-x=" + -x);

		x = 10;
		System.out.println("+x=" + +x);
		System.out.println("-x=" + -x);

	}

}
